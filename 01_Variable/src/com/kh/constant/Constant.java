package com.kh.constant;

public class Constant {
	// * 상수
	// 변하지 않는 값(수학 익힘책)
	public static void main(String[] args) {
		
		// 정수(양수, 음수)
		System.out.println(7);
		System.out.println('7'); //컴퓨터는 단일문자로 인식
		System.out.println("7"); //컴퓨터는 문자열로 인식 //Date type 자료형이라 부른다. // 변수를 시작하도록 하겠습니다.
		System.out.println(-7);
		
		System.out.println("------------------------");
		
		// 실수(.)
		System.out.println(0.7);
		System.out.println(-0.7);
		
		// (단일)문자'', 문자열""
		System.out.println('금');
		System.out.println("금 1000돈");
		
		// 논리값(true, false)
		System.out.println(true);
		System.out.println(false);
	
	}
}










