	// 확인을 한 뒤에 결과를 View로 다시 반환을 해줘야 한다.
	public boolean checkId(String userId) {
		
		// 1. 입력한 아이디 => userId
		// 2. Fan들이 들어가는 List => fans
		// 3. 요소의 userId 필드값 => fans.get(i).getUserId()
		// 4. 중복되는 게 있는지 => equals()
		
		for(int i = 0; i < fans.size(); i++) {
			if(userId.equals(fans.get(i).getUserId())) {
			
				return true; 
			}
		}
		return false;
	}
	
