		// 1. 데이터 가공 (값이 여러개면 다루기가 힘드니 따로 VO를 쓰거나 MAP을 쓰는 게 보편적)
		// 지금 같은 경우에는 회원의 정보를 받아와서 FAN 이라는 모양으로 다루기로 했단 말입니다.
		Fan fan = new Fan(userId, userPwd, nickName);
		
		// 2. 요청 처리(아직 요청 처리하는 Layer(계층)를 안배웠으므로 컨트롤러에서 처리)
		boolean result = checkId(userId);
		
